# Arithmetic operators .performs simple calculations
x = 45
y = 21

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  # Modulus . Returns the remainder

# Comparison operators
a = 56
b = 2
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)  # equal to
print(a != b)  # not equal to

# Assignment operators
z = 6
print(z)

num1 = 2
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not (num1 < num2 and num1 == num2))


