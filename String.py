text = "Hello"
text1 = "THERE"

print(text)
# Accessing an element in a string
print(text[1])

# modifying a string
print(text.upper())
print(text1.lower())

# size/length of a string
print(len(text))

# string concatenation combining strings
print(text + " " + text1)

# reassign a string
text = " You're welcome"
print(text)

# deleting a string
text1 = ""
print(text1)
